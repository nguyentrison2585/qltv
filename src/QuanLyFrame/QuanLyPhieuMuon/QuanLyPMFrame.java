    public void setCenterRenderer(JTable table) {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment((int) JTable.CENTER_ALIGNMENT);
        for (int column=0;column<table.getColumnCount();column++) {
            table.getColumnModel().getColumn(column).setCellRenderer(centerRenderer);
        }
    }
    
    public void loadData() {
        model = (DefaultTableModel)jTable1.getModel();
        model = con.getData(model,"phieu_muon");
    }
    
    public void disPlay() {
        this.setTitle("Nguyễn Trí Sơn - 20166683");
        this.setVisible(true);
        this.setCenterRenderer(jTable1);
        this.setLocationRelativeTo(null);
        this.setResizable(false);
        maBDList = con.getData("phieu_muon", "ma_ban_doc");
        maNVList = con.getData("phieu_muon", "ma_nhan_vien");
        ngayMuonList = con.getData("phieu_muon", "ngay_muon");
        for (String maBD:maBDList) {
            jComboBox1.addItem(maBD);
        }
        for (String maNV:maNVList) {
            jComboBox3.addItem(maNV);
        }
        for (String ngayMuon:ngayMuonList) {
            jComboBox4.addItem(ngayMuon);
        }
        this.jRadioButton2.setSelected(true);
        this.jComboBox3.disable();
        this.jComboBox4.disable();
        this.loadData();
    }
