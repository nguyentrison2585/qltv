    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()<0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn một phiếu mượn để xóa!",
                    "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else {
            int row = jTable1.getSelectedRow();
            int select = JOptionPane.showOptionDialog(null, "Bạn muốn xóa phiếu mượn đã chọn",
                    "Delete",0,JOptionPane.YES_NO_OPTION,null,null,1);
            if (select == 0) {
                con.deleteTTMT((String)jTable1.getValueAt(row, 0));
                con.delete("phieu_muon", (String)jTable1.getValueAt(row, 0));
                loadData();
            }    
        }
    }//GEN-LAST:event_jButton3ActionPerformed

