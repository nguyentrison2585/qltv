    public void setCenterRenderer(JTable table) {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment((int) JTable.CENTER_ALIGNMENT);
        for (int column=0;column<table.getColumnCount();column++) {
            table.getColumnModel().getColumn(column).setCellRenderer(centerRenderer);
        }
    }
    
    public void loadData() {
        model = (DefaultTableModel)jTable1.getModel();
        model = con.getData(model, "muon_tra");
    }
    
    public void disPlay() {
        this.setVisible(true);
        this.setCenterRenderer(jTable1);
        this.setLocationRelativeTo(null);
        this.setResizable(false);
        this.maPMList = con.getData("muon_tra", "ma_phieu_muon");
        this.maSachList = con.getData("muon_tra", "ma_sach");
        for (String maPM:maPMList) {
            jComboBox1.addItem(maPM);
        }
        for (String maSach:maSachList) {
            jComboBox2.addItem(maSach);
        }
        this.jComboBox1.disable();
        this.jComboBox2.disable();
        this.loadData();
    }
