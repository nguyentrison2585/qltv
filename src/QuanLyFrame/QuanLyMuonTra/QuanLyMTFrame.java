    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        if (jComboBox1.getSelectedIndex()==0) {
            loadData();
        } else {
            try {
                con.command = "select * from muon_tra where ma_phieu_muon = '" + jComboBox1.getSelectedItem() + "'";
                con.result = con.state.executeQuery(con.command);
                ResultSetMetaData rsMD = con.result.getMetaData();
                int colCount = rsMD.getColumnCount();
                String arr[] = new String[colCount];
                while (con.result.next()) {
                    for(int i=0;i<colCount;i++) {
                        arr[i] = con.result.getString(i+1);
                    }
                    model.addRow(arr);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Lỗi tìm kiếm", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

