    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()<0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn một nhân viên để cập nhật thông tin!",
                    "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else {
            capNhat = new CapNhatNVFrame();
            capNhat.disPlay();
            int row = jTable1.getSelectedRow();
            capNhat.jTextField1.setText((String)jTable1.getValueAt(row, 1));
            capNhat.jTextField2.setText((String)jTable1.getValueAt(row, 2));
            capNhat.jTextField3.setText((String)jTable1.getValueAt(row, 3));
            capNhat.jComboBox1.setSelectedItem(jTable1.getValueAt(row, 4));
            capNhat.jTextField4.setText((String)jTable1.getValueAt(row, 5));
            capNhat.jTextField5.setText((String)jTable1.getValueAt(row, 6));
            capNhat.jTextField6.setText((String)jTable1.getValueAt(row, 7));
            capNhat.jTextField7.setText((String)jTable1.getValueAt(row, 8));
            capNhat.addWindowListener(new WindowAdapter() {
                @Override
                public void windowClosed(WindowEvent evt) {
                    loadData();
                }
        });
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()<0) {
            JOptionPane.showMessageDialog(null, "Bạn phải chọn một nhân viên để xóa!",
                    "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else {
            int row = jTable1.getSelectedRow();
            int select = JOptionPane.showOptionDialog(null, "Bạn muốn xóa nhân viên đã chọn",
                    "Delete",0,JOptionPane.YES_NO_OPTION,null,null,1);
            if (select == 0) {
                con.delete("nhan_vien", (String)jTable1.getValueAt(row, 0));
                loadData();
            }    
        }
    }//GEN-LAST:event_jButton3ActionPerformed

