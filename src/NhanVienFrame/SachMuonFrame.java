    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row<0) {
            JOptionPane.showMessageDialog(null, "Bạn cần chọn một dòng để bỏ","Warning",JOptionPane.WARNING_MESSAGE);
        } else {
            int select = JOptionPane.showOptionDialog(null, "Bạn muốn bỏ cuốn sách này khỏi danh sách ?", "Delete",
                    0, JOptionPane.YES_NO_OPTION, null, null, 1);
            if(select == 0) {
                model.removeRow(row);
                for (int i=0;i<model.getRowCount();i++) {
                    model.setValueAt(i+1,i,0);
                }
                count--;
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

