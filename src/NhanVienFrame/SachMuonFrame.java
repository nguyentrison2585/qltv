    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (!jTextField2.getText().equals("")) {
                boolean check = true;
                for (int i=0;i<jTable1.getRowCount();i++) {
                    if (jComboBox1.getSelectedItem().toString().equals(jTable1.getValueAt(i, 1))) {
                        check = false;
                    }
                }
                if (!check) {
                    JOptionPane.showMessageDialog(null, "Không thể mượn 2 cuốn sách giống nhau",
                            "Warning",JOptionPane.WARNING_MESSAGE);
                }
                else {
                    con.command = "select * from sach where id ='" + jComboBox1.getSelectedItem() + "'";
                    con.result = con.state.executeQuery(con.command);
                    model = (DefaultTableModel)jTable1.getModel();
                    if (con.result.next()) {
                        count++;
                        id = con.result.getString("id");
                        ten_sach = con.result.getString("ten_sach");
                        the_loai = con.result.getString("the_loai");
                        tac_gia = con.result.getString("ten_tac_gia");
                        Object row[] = {count,id,ten_sach,the_loai,tac_gia};
                        model.addRow(row);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Thư viện không có cuốn sách này","Warning",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

